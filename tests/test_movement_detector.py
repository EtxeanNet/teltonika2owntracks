from datetime import datetime
from teltonika_owntracks.movement_detector import MovementDetector

def test_has_moved_standstill():
    start = datetime.timestamp(datetime(2024, 2,24, 12, 0, 0))
    now = start
    detector = MovementDetector(60, 25, lambda: now)
    movements = []
    for lat, lon in get_history_standstill():
        now += 5
        movements.append(detector.has_moved(now, lat, lon))
    assert not any(movements)

def test_has_moved_moving():
    start = datetime.timestamp(datetime(2024, 2,24, 12, 0, 0))
    now = start
    detector = MovementDetector(60, 25, lambda: now)
    movements = []
    for lat, lon in get_history_moving():
        now += 5
        movements.append(detector.has_moved(now, lat, lon))
    # there has been movement
    assert any(movements)
    # but not moving now
    assert detector.has_moved(now, 51.470000,5.500561) == False        

